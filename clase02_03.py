# Lista de años de análisis
periodo = [2020,2021,2022,2023,2024]
ventas = [1500,1600,800,1700,1300]

# Listar los periodos - Horizontal
print(periodo)

# Listar los periodos - vertical
for p in periodo:
    print(p)

# Listar las ventas - vertical
# v es una variable que tomara los valortes de la lista X
for v in ventas:
    print(v)